package org.xxxmathxxx.tddt.profile;

/**The MedalState enum describes the different medal-ranks a user can have at a given exercise
 * @author xxxMathxxx 2016
 *
 */
public enum MedalState {
	/**
	 * Self-Explaining, this means you have no medal yet at the given exercise
	 */
	NONE,
	/**
	 * The bronze medal
	 */
	BRONZE,
	/**
	 * The silver medal
	 */
	SILVER,
	/**
	 * The gold medal
	 */
	GOLD,
	/**
	 * The (hidden) author medal
	 */
	AUTHOR;
	
	
}
